/**
 * Builds a self-hosted Remix Icon subset (woff2 + CSS) from icons referenced in src/.
 * Replaces the full 173KB CDN font with only glyphs this app uses (~160 icons).
 *
 * Run automatically before `next build`, or manually:
 *   node scripts/build-remixicon-subset.mjs
 */

import { readFile, writeFile, mkdir, readdir } from "node:fs/promises";
import { createRequire } from "node:module";
import path from "node:path";
import { fileURLToPath } from "node:url";
import subsetFont from "subset-font";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(__dirname, "..");
const require = createRequire(import.meta.url);

const remixiconPkg = path.dirname(require.resolve("remixicon/package.json"));
const remixCssPath = path.join(remixiconPkg, "fonts", "remixicon.css");
const remixWoff2Path = path.join(remixiconPkg, "fonts", "remixicon.woff2");

const outDir = path.join(appRoot, "public", "fonts");
const publicStylesDir = path.join(appRoot, "public", "styles");
const outCssPath = path.join(publicStylesDir, "remixicon-subset.css");
const outWoff2Name = "remixicon-subset.woff2";

/** @param {string} css */
function parseIconRules(css) {
  /** @type {Map<string, string>} */
  const map = new Map();
  const re = /\.(ri-[a-z0-9-]+):before\s*\{\s*content:\s*"\\([0-9a-fA-F]+)"/g;
  let m;
  while ((m = re.exec(css)) !== null) {
    map.set(m[1], m[2].toUpperCase());
  }
  return map;
}

/** @param {string} dir */
async function walkSourceFiles(dir, acc = []) {
  const entries = await readdir(dir, { withFileTypes: true });
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      await walkSourceFiles(full, acc);
    } else if (/\.(tsx?)$/.test(entry.name)) {
      acc.push(full);
    }
  }
  return acc;
}

/** @param {string} appDir */
async function collectUsedIconClasses(appDir) {
  const srcDir = path.join(appDir, "src");
  const files = (await walkSourceFiles(srcDir)).filter(
    (f) => !f.endsWith(`${path.sep}wp-redirects.ts`),
  );
  const found = new Set();
  const re = /\b(ri-[a-z0-9-]+)\b/g;
  for (const file of files) {
    const text = await readFile(file, "utf8");
    let match;
    while ((match = re.exec(text)) !== null) {
      found.add(match[1]);
    }
  }
  return found;
}

async function main() {
  const fullCss = await readFile(remixCssPath, "utf8");
  const iconRules = parseIconRules(fullCss);
  const usedClasses = await collectUsedIconClasses(appRoot);

  const matched = [];
  const missing = [];
  for (const cls of [...usedClasses].sort()) {
    if (iconRules.has(cls)) {
      matched.push(cls);
    } else if (
      cls.startsWith("ri-") &&
      !cls.includes("may-get") &&
      cls !== "ri-link" &&
      cls !== "ri-text" &&
      cls !== "ri-separator" &&
      !/^ri-h-\d$/.test(cls) &&
      !/^ri-list-/.test(cls)
    ) {
      missing.push(cls);
    }
  }

  if (missing.length > 0) {
    console.warn(
      `[remixicon-subset] ${missing.length} class(es) not in remixicon.css (skipped):`,
      missing.slice(0, 8).join(", "),
      missing.length > 8 ? "…" : "",
    );
  }

  const codepoints = [...new Set(matched.map((cls) => iconRules.get(cls)))];
  const chars = codepoints.map((hex) => String.fromCodePoint(parseInt(hex, 16))).join("");

  const fontBuffer = await readFile(remixWoff2Path);
  const subsetBuffer = await subsetFont(fontBuffer, chars, { targetFormat: "woff2" });

  await mkdir(outDir, { recursive: true });
  await mkdir(publicStylesDir, { recursive: true });
  await writeFile(path.join(outDir, outWoff2Name), subsetBuffer);

  const sizeRules = fullCss
    .split("\n")
    .filter((line) => line.startsWith(".ri-lg ") || line.startsWith(".ri-xl ") || line.startsWith(".ri-xxs "))
    .join("\n");

  const iconCss = matched
    .map((cls) => {
      const hex = iconRules.get(cls).toLowerCase();
      return `.${cls}:before { content: "\\${hex}"; }`;
    })
    .join("\n");

  const subsetCss = `/* Remix Icon subset — generated by scripts/build-remixicon-subset.mjs */
@font-face {
  font-family: "remixicon";
  src: url("/fonts/${outWoff2Name}") format("woff2");
  font-display: swap;
}

[class^="ri-"], [class*=" ri-"] {
  font-family: "remixicon" !important;
  font-style: normal;
  -webkit-font-smoothing: antialiased;
  -moz-osx-font-smoothing: grayscale;
}

${sizeRules}

${iconCss}
`;

  await writeFile(outCssPath, subsetCss);

  const fullKb = (fontBuffer.length / 1024).toFixed(1);
  const subsetKb = (subsetBuffer.length / 1024).toFixed(1);
  console.log(
    `[remixicon-subset] ${matched.length} icons → ${subsetKb} KB woff2 (was ${fullKb} KB full font)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
