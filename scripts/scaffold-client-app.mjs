#!/usr/bin/env node
/**
 * Scaffold apps/<slug> from apps/client-template (copy, package name, next.config host,
 * .env.local, optional brand string replacements).
 *
 * Usage:
 *   node scripts/scaffold-client-app.mjs \
 *     --slug my-brand \
 *     --name "My Brand" \
 *     --url https://mybrand.com \
 *     --ref <project_ref> \
 *     --anon-key <anon_key>
 *
 *   --force              Overwrite existing apps/<slug>
 *   --no-brand-replace   Skip replacing Client Brand / example.com placeholders in src + public
 *   --contact-email      Override default hello@<apex-domain>
 */

import {
  cpSync,
  existsSync,
  readFileSync,
  readdirSync,
  rmSync,
  statSync,
  writeFileSync,
} from 'fs';
import { join, dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');

const SKIP_NAMES = new Set([
  'node_modules',
  '.next',
  '.turbo',
  'dist',
  'coverage',
  '.git',
]);

function getArg(flag) {
  const i = process.argv.indexOf(flag);
  return i >= 0 ? process.argv[i + 1] : null;
}

function hasFlag(flag) {
  return process.argv.includes(flag);
}

/**
 * @param {object} opts
 * @param {string} opts.slug
 * @param {string} opts.displayName  Brand display name (replaces "Client Brand")
 * @param {string} opts.siteUrl      Canonical site URL, e.g. https://example.com
 * @param {string} opts.supabaseRef  Project ref (subdomain before .supabase.co)
 * @param {string} opts.anonKey      NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY / anon key
 * @param {boolean} [opts.force]
 * @param {boolean} [opts.brandReplace=true]
 * @param {string} [opts.contactEmail]
 */
export async function runScaffold(opts) {
  const {
    slug,
    displayName,
    siteUrl,
    supabaseRef,
    anonKey,
    force = false,
    brandReplace = true,
    contactEmail: contactEmailOpt,
  } = opts;

  if (!slug || !/^[a-z0-9-]+$/.test(slug)) {
    throw new Error('Invalid or missing slug (lowercase letters, numbers, hyphens only).');
  }
  if (!displayName?.trim()) throw new Error('Missing displayName.');
  if (!siteUrl?.trim()) throw new Error('Missing siteUrl.');
  if (!supabaseRef?.trim()) throw new Error('Missing supabaseRef.');
  if (!anonKey?.trim()) throw new Error('Missing anonKey.');

  let parsedUrl;
  try {
    parsedUrl = new URL(siteUrl);
  } catch {
    throw new Error(`Invalid siteUrl: ${siteUrl}`);
  }

  const templateDir = join(REPO_ROOT, 'apps', 'client-template');
  const destDir = join(REPO_ROOT, 'apps', slug);

  if (!existsSync(templateDir)) {
    throw new Error(`Template not found: ${templateDir}`);
  }

  if (existsSync(destDir)) {
    if (!force) {
      throw new Error(`Destination already exists: ${destDir}\n  Use force: true or delete the folder.`);
    }
    rmSync(destDir, { recursive: true, force: true });
  }

  cpSync(templateDir, destDir, {
    recursive: true,
    filter: (srcPath) => {
      const segments = srcPath.split(/[/\\]/);
      return !segments.some((seg) => SKIP_NAMES.has(seg));
    },
  });

  const pkgPath = join(destDir, 'package.json');
  const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
  pkg.name = `@sweetmedia/${slug}`;
  writeFileSync(pkgPath, `${JSON.stringify(pkg, null, 2)}\n`, 'utf8');

  const nextPath = join(destDir, 'next.config.ts');
  let nextSrc = readFileSync(nextPath, 'utf8');
  nextSrc = nextSrc.replace(
    /hostname:\s*"[^"]*\.supabase\.co"/g,
    `hostname: "${supabaseRef}.supabase.co"`,
  );
  writeFileSync(nextPath, nextSrc, 'utf8');

  const apexHost = parsedUrl.hostname.replace(/^www\./, '');
  const contactEmail = contactEmailOpt?.trim() || `hello@${apexHost}`;

  const envLines = [
    `# Generated by scaffold-client-app`,
    `NEXT_PUBLIC_SUPABASE_URL=https://${supabaseRef}.supabase.co`,
    `NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY=${anonKey}`,
    `NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}`,
    `NEXT_PUBLIC_SITE_ID=${slug}`,
    `NEXT_PUBLIC_SITE_URL=${siteUrl.replace(/\/$/, '')}`,
    ``,
    `# Surfer SEO (set in Vercel env vars; leave commented locally unless testing)`,
    `# SURFER_API_KEY=`,
    `# SURFER_PROJECT_ID=`,
    `# NEXT_PUBLIC_BLOG_PATH_BASE=/blog`,
    ``,
    `# Vercel Cron auth — generate a random string per project (e.g. openssl rand -hex 32)`,
    `# CRON_SECRET=`,
    '',
  ];
  writeFileSync(join(destDir, '.env.local'), envLines.join('\n'), 'utf8');

  if (brandReplace) {
    replaceBrandStrings(destDir, {
      slug,
      displayName,
      siteUrl: siteUrl.replace(/\/$/, ''),
      supabaseRef,
      anonKey,
      contactEmail,
    });
  }

  return {
    destDir,
    packageName: `@sweetmedia/${slug}`,
    envPath: join(destDir, '.env.local'),
  };
}

/**
 * Replace template literals in text files under src/ and public/ (excluding binary-ish extensions).
 * Also patches specific files that have hard-coded Inner Peak / template Supabase refs.
 */
function replaceBrandStrings(appRoot, { slug, displayName, siteUrl, supabaseRef, anonKey, contactEmail }) {
  const roots = [join(appRoot, 'src'), join(appRoot, 'public'), join(appRoot, 'docs')]
    .filter((p) => existsSync(p));
  const textExt = /\.(tsx?|jsx?|mjs|cjs|css|md|txt|json)$/i;

  // The Inner Peak Supabase ref that bleeds through from shared defaults
  const INNER_PEAK_REF = 'ynmldknprfusujudvutq';
  const apexDomain = siteUrl.replace(/^https?:\/\/(www\.)?/, '').replace(/\/.*$/, '');

  function walk(dir) {
    for (const name of readdirSync(dir)) {
      const full = join(dir, name);
      const st = statSync(full);
      if (st.isDirectory()) {
        walk(full);
        continue;
      }
      if (!textExt.test(name)) continue;
      if (name === 'package.json' || name === 'package-lock.json') continue;

      let s = readFileSync(full, 'utf8');
      const before = s;

      // Core brand name replacements
      s = s.split('Client Brand').join(displayName);
      s = s.split('hello@example.com').join(contactEmail);
      s = s.split('https://example.com').join(siteUrl);

      // Fix ogDefaults.ts — remove any stale Inner Peak Supabase URL
      if (full.endsWith('ogDefaults.ts')) {
        s = s.replace(
          /https:\/\/ynmldknprfusujudvutq\.supabase\.co\/[^\s"']*/g,
          `https://${supabaseRef}.supabase.co/storage/v1/object/public/site-assets/images/og-default.jpg`,
        );
      }

      // Fix markdownToBlog.ts — remove stale Inner Peak author photo default
      if (full.endsWith('markdownToBlog.ts')) {
        s = s.replace(
          /authorPhoto: frontmatter\.authorPhoto \|\| ["']https:\/\/ynmldknprfusujudvutq[^"']*["']/g,
          `authorPhoto: frontmatter.authorPhoto || ""`,
        );
        s = s.replace(
          /author: frontmatter\.author \|\| ["']Client Brand["']/g,
          `author: frontmatter.author || "${displayName}"`,
        );
        s = s.replace(
          /authorRole: frontmatter\.authorRole \|\| ["']Content Team["']/g,
          `authorRole: frontmatter.authorRole || "Editorial Team"`,
        );
        s = s.replace(
          /authorBio: frontmatter\.authorBio \|\| ["']Client-centered[^"']*["']/g,
          `authorBio: frontmatter.authorBio || "Helpful educational resources from ${displayName}."`,
        );
      }

      // Fix admin/layout.tsx — replace "Client Brand Admin" brandName
      if (full.includes('admin') && full.endsWith('layout.tsx')) {
        s = s.replace(
          /brandName="Client Brand Admin"/g,
          `brandName="${displayName}"`,
        );
        s = s.replace(
          /brandInitial="C"/g,
          `brandInitial="${displayName.split(' ').map(w=>w[0]).join('').slice(0,2).toUpperCase()}"`,
        );
      }

      // Fix blog [slug] metadata — "Client Brand" in OG siteName / description
      if (full.includes('blog') && full.includes('[slug]') && full.endsWith('page.tsx')) {
        s = s.replace(/siteName: "Client Brand"/g, `siteName: "${displayName}"`);
        s = s.replace(/from Client Brand\./g, `from ${displayName}.`);
      }

      // Fix BlogEditorSidebar SEO preview domain
      if (full.endsWith('BlogEditorSidebar.tsx')) {
        s = s.replace(/sweetmedia\.com\/blog\//g, `${apexDomain}/blog/`);
        s = s.replace(/\| Client Brand/g, `| ${displayName}`);
      }

      if (s !== before) writeFileSync(full, s, 'utf8');
    }
  }

  for (const r of roots) walk(r);
}

/** Normalize Supabase Management API response for API keys list */
export function parseApiKeysResponse(raw) {
  if (Array.isArray(raw)) return raw;
  if (raw && Array.isArray(raw.api_keys)) return raw.api_keys;
  if (raw && Array.isArray(raw.keys)) return raw.keys;
  return [];
}

async function cliMain() {
  const slug = getArg('--slug');
  const name = getArg('--name');
  const url = getArg('--url');
  const ref = getArg('--ref');
  const anonKey = getArg('--anon-key');
  const force = hasFlag('--force');
  const brandReplace = !hasFlag('--no-brand-replace');
  const contactEmail = getArg('--contact-email');

  if (!slug || !name || !url || !ref || !anonKey) {
    console.error(`
Usage:
  node scripts/scaffold-client-app.mjs \\
    --slug my-brand \\
    --name "My Brand" \\
    --url https://mybrand.com \\
    --ref <supabase_project_ref> \\
    --anon-key <anon_public_key>

Options:
  --force
  --no-brand-replace
  --contact-email hello@mybrand.com
`);
    process.exit(1);
  }

  try {
    const out = await runScaffold({
      slug,
      displayName: name,
      siteUrl: url,
      supabaseRef: ref,
      anonKey,
      force,
      brandReplace,
      contactEmail: contactEmail || undefined,
    });
    console.log(`\n✅  Scaffolded ${out.packageName}`);
    console.log(`    ${out.destDir}`);
    console.log(`    ${out.envPath}\n`);
  } catch (e) {
    console.error(e instanceof Error ? e.message : e);
    process.exit(1);
  }
}

const entryFile = process.argv[1] ? resolve(process.argv[1]) : '';
const thisFile = fileURLToPath(import.meta.url);
if (entryFile && entryFile === thisFile) {
  cliMain();
}
