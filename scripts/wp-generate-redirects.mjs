#!/usr/bin/env node
/**
 * Generate Next.js redirect rules from a redirect map CSV.
 *
 * Reads:  apps/<slug>-migration/redirect-map.csv  (cols: from_path,to_path,...)
 * Writes: apps/<slug>/src/lib/wp-redirects.ts     (exports an array of NextRedirect)
 *
 * The output file can be imported into `next.config.ts` like:
 *
 *     import { wpRedirects } from "./src/lib/wp-redirects";
 *     export default {
 *       async redirects() { return wpRedirects; }
 *     } satisfies NextConfig;
 *
 * All redirects are emitted with `permanent: true` (301).
 *
 * Usage:
 *   node scripts/wp-generate-redirects.mjs \
 *     --map apps/<slug>-migration/redirect-map.csv \
 *     --out apps/<slug>/src/lib/wp-redirects.ts
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname } from 'node:path';

function getArg(name, fallback) {
  const i = process.argv.indexOf(name);
  return i > -1 ? process.argv[i + 1] : fallback;
}

const mapPath = getArg('--map');
const outPath = getArg('--out');

if (!mapPath || !outPath) {
  console.error('Usage: --map <redirect-map.csv> --out <output.ts>');
  process.exit(1);
}

function parseCSV(raw) {
  const lines = raw.split(/\r?\n/).filter(Boolean);
  const header = lines[0].split(',');
  return lines.slice(1).map((line) => {
    const cells = [];
    let cur = '';
    let inQuotes = false;
    for (let i = 0; i < line.length; i++) {
      const ch = line[i];
      if (ch === '"' && line[i + 1] === '"') { cur += '"'; i++; continue; }
      if (ch === '"') { inQuotes = !inQuotes; continue; }
      if (ch === ',' && !inQuotes) { cells.push(cur); cur = ''; continue; }
      cur += ch;
    }
    cells.push(cur);
    const row = {};
    header.forEach((h, idx) => { row[h.trim()] = cells[idx]?.trim() ?? ''; });
    return row;
  });
}

const rows = parseCSV(readFileSync(mapPath, 'utf8'));

// De-dupe by source path; warn on conflicts.
const bySource = new Map();
for (const r of rows) {
  const from = (r.from_path || '').trim();
  const to = (r.to_path || '').trim();
  if (!from || !to) continue;
  if (bySource.has(from) && bySource.get(from).to !== to) {
    console.warn(`⚠️  Duplicate redirect source with conflicting destinations: ${from}\n   keeping: ${bySource.get(from).to}\n   ignored: ${to}`);
    continue;
  }
  bySource.set(from, { from, to, quality: r.quality, type: r.type });
}

// Sort by source path for deterministic output
const sorted = [...bySource.values()].sort((a, b) => a.from.localeCompare(b.from));

const header = `// AUTO-GENERATED — do not edit by hand.
// Generated by: scripts/wp-generate-redirects.mjs
// Source: ${mapPath.replace(/^.*[\\/]/, '')}
// Total: ${sorted.length} redirects

export type NextRedirect = {
  source: string;
  destination: string;
  permanent: boolean;
};

export const wpRedirects: NextRedirect[] = [
`;

const body = sorted.map((r) => {
  const note = r.quality ? ` // ${r.quality}` : '';
  return `  { source: ${JSON.stringify(r.from)}, destination: ${JSON.stringify(r.to)}, permanent: true },${note}`;
}).join('\n');

const footer = '\n];\n';

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, header + body + footer);

console.log(`Wrote ${sorted.length} redirects to ${outPath}`);
